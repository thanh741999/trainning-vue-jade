export function DisplayNameToId (name) {
    if (name === 'Thuốc không kê toa' ) {
        return 1
    } else if (name=== 'Thực phẩm chức năng') {
        return 2
    } else if (name === 'Dụng cụ y khoa') {
        return 3
    } else if (name === 'Mỹ phẩm') {
        return 4
    } else return 5
}